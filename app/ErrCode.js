// 400	（错误请求）服务器不理解请求的语法。
// 401	（未授权）请求要求身份验证。对于需要token的接口，服务器可能返回此响应。
// 403	（禁止）服务器拒绝请求。对于群组/聊天室服务，表示本次调用不符合群组/聊天室操作的正确逻辑，例如调用添加成员接口，添加已经在群组里的用户，或者移除聊天室中不存在的成员等操作。
// 404	（未找到）服务器找不到请求的接口。
// 408	（请求超时）服务器等候请求时发生超时。
// 413	（请求体过大）请求体超过了5kb，拆成更小的请求体重试即可。
// 415	请求体的类型不支持。
// 429	（服务不可用）请求接口超过调用频率限制，即接口被限流。或超过社区版限制，如有需要可联系商务。
// 500	（服务器内部错误）服务器遇到错误，无法完成请求。
// 501	（尚未实施）服务器不具备完成请求的功能。例如，服务器无法识别请求方法时可能会返回此代码。
// 502	（错误网关）服务器作为网关或代理，从上游服务器收到无效响应。
// 503	（服务不可用）请求接口超过调用频率限制，即接口被限流。
// 504	（网关超时）服务器作为网关或代理，但是没有及时从上游服务器收到请求。
export const 错误请求=400;
export const 未授权=401;
export const 未找到=404;
export const 请求超时=408;
export const 请求体过大=413;
export const 请求体的类型不支持=415;
export const 服务不可用=429;
export const 服务器内部错误=500;
export const 尚未实施=501;
export const 错误网关=502;
export const 服务超出限制=503;
export const 网关超时=504;